        function playSoundEffect(combo) {
            // Web Audio API„ÅÆÂàùÊúüÂåñÔºà„É¶„Éº„Ç∂„ÉºÊìç‰ΩúÂæå„Å´ÂàùÊúüÂåñÔºâ
            if (!audioContext) {
                try {
                    audioContext = new (window.AudioContext || window.webkitAudioContext)();
                } catch (e) {
                    return; // Web Audio API„ÅåÂà©Áî®„Åß„Åç„Å™„ÅÑ
                }
            }

            // „Çµ„Çπ„Éö„É≥„ÉâÁä∂ÊÖã„ÅÆÂ†¥Âêà„ÅØÂÜçÈñã
            if (audioContext.state === 'suspended') {
                audioContext.resume();
            }

            try {
                const oscillator = audioContext.createOscillator();
                const gainNode = audioContext.createGain();

                oscillator.connect(gainNode);
                gainNode.connect(audioContext.destination);

                // „Ç≥„É≥„ÉúÊï∞„Å´Âøú„Åò„Å¶Èü≥Á®ã„ÇíÂ§â„Åà„Çã
                const baseFreq = 200;
                const freq = baseFreq + (combo * 50);
                oscillator.frequency.value = Math.min(freq, 800);
                oscillator.type = combo >= 3 ? 'sine' : 'square';

                gainNode.gain.setValueAtTime(0.1, audioContext.currentTime);
                gainNode.gain.exponentialRampToValueAtTime(0.01, audioContext.currentTime + 0.1);

                oscillator.start(audioContext.currentTime);
                oscillator.stop(audioContext.currentTime + 0.1);
            } catch (e) {
                console.log('„Çµ„Ç¶„É≥„Éâ„Ç®„Éï„Çß„ÇØ„Éà„ÅÆÂÜçÁîü„Å´Â§±Êïó„Åó„Åæ„Åó„Åü');
            }
        }

